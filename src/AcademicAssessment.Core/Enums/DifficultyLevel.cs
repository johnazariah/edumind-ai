namespace AcademicAssessment.Core.Enums;

/// <summary>
/// Difficulty levels for questions and assessments
/// </summary>
public enum DifficultyLevel
{
    /// <summary>
    /// Very easy difficulty (below grade level)
    /// </summary>
    VeryEasy = 0,

    /// <summary>
    /// Easy difficulty (lower end of grade level)
    /// </summary>
    Easy = 1,

    /// <summary>
    /// Medium difficulty (appropriate for grade level)
    /// </summary>
    Medium = 2,

    /// <summary>
    /// Hard difficulty (upper end of grade level)
    /// </summary>
    Hard = 3,

    /// <summary>
    /// Very hard difficulty (above grade level)
    /// </summary>
    VeryHard = 4
}
